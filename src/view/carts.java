    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed

        try {
            JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\LENOVO\\Documents\\NetBeansProjects\\Vesmart\\src\\view\\reportreceipt.jrxml");
            String query = "SELECT * FROM cart";

            JRDesignQuery updateQuery = new JRDesignQuery();
            updateQuery.setText(query);

            jdesign.setQuery(updateQuery);

            JasperReport jreport = JasperCompileManager.compileReport(jdesign);
            JasperPrint jprint = JasperFillManager.fillReport(jreport, null, cn);
            JasperViewer.viewReport(jprint, rootPaneCheckingEnabled, Locale.FRENCH);
        } catch(Exception e) {
            e.printStackTrace();}
    }//GEN-LAST:event_btnPrintActionPerformed

