    public void tampilData(String where) {
        
        try {
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM cart " + where);

            while (rs.next()) {
                Object[] data = {
                    rs.getString("Name"),
                    rs.getString("Phone"),
                    rs.getString("Address"),
                    rs.getString("Type"),
                    rs.getString("Color"),
                    rs.getString("Price"),
                    rs.getString("Quantity"),
                    rs.getString("Subtotal"),
                    rs.getString("Total"),
                    rs.getString("Pay"),
                    rs.getString("Outcome"),
                    rs.getString("Date"),
                    
                };

                tabModel.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void total(){
        
        int total = 0;
        for (int i = 0; i < tableCart.getRowCount(); i++){
            int amount = Integer.parseInt((String)tableCart.getValueAt(i, 7));
            total += amount;
        }
        txtTotal.setText("" + total);
    }
    
    public void pay(){
        
        int total = Integer.parseInt(txtTotal.getText());
        int pay = Integer.parseInt(txtPay.getText());

        if (pay < total) {
            JOptionPane.showMessageDialog(null, "The payment amount must be greater than the total price!");
            txtPay.setText("");
        } else {
            int change = pay - total;
            txtOutcome.setText(Integer.toString(change));
        }
    }
    
    public void date(){
        // Mendapatkan tanggal saat ini
        Date currentDate = new Date();

        // Mengatur format tanggal yang diinginkan (misalnya: "dd-MM-yyyy HH:mm:ss")
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");

        // Mengisi nilai tanggal ke dalam JTextField dengan nama txtDate
        txtDate.setText(dateFormat.format(currentDate));
    }

